package com.macor.comandas.api.model;

public class Commands {

    private String name;
    private String number;

    public Commands() {

    }

    public void setName(String name) {
        this.name = name;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }
}
